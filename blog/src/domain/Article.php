<?php

namespace doncredasPHP\blog\src\domain;


class Article {

    /**
     * Article id
     * @var string
     */
    private $id;

    /**
     * Article title
     * @var string
     */
    private $titre;

    /**
     * Article content
     * @var string
     */
    private $contenu;

    /**
     * @return string
     */
    public function getTitre()
    {
        return $this->titre;
    }

    /**
     * @param string $titre
     */
    public function setTitre($titre)
    {
        $this->titre = $titre;
    }

    /**
     * @return string
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param string $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getContenu()
    {
        return $this->contenu;
    }

    /**
     * @param string $contenu
     */
    public function setContenu($contenu)
    {
        $this->contenu = $contenu;
    }

}